# Arithmetic Operators Examples
# Arithmetic operators perform mathematical operations

# Addition
sum_result = 10 + 5
print("10 + 5 =", sum_result)

# Subtraction
diff = 10 - 5
print("10 - 5 =", diff)

# Multiplication
product = 10 * 5
print("10 * 5 =", product)

# Division (returns float)
quotient = 10 / 5
print("10 / 5 =", quotient)

# Floor Division (returns integer)
floor_div = 10 // 3
print("10 // 3 =", floor_div)

# Modulus (remainder)
remainder = 10 % 3
print("10 % 3 =", remainder)

# Exponentiation (power)
power = 2 ** 3
print("2 ** 3 =", power)

# Order of operations (PEMDAS)
result = 2 + 3 * 4
print("2 + 3 * 4 =", result)  # 14

result = (2 + 3) * 4
print("(2 + 3) * 4 =", result)  # 20

# Combined operations with variables
x = 15
y = 4
print(f"x + y = {x + y}")
print(f"x - y = {x - y}")
print(f"x * y = {x * y}")
print(f"x / y = {x / y}")
print(f"x // y = {x // y}")
print(f"x % y = {x % y}")
print(f"x ** y = {x ** y}")
