# Elif Statement Examples
# The elif (else if) statement handles multiple conditions

# Simple if-elif-else
score = 78
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: F")
