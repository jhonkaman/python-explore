# Print multiple values separated by commas
print("Name:", "Javier", "Age:", 25)

# Print with different data types
print("Number:", 42)
print("Float:", 3.14)
print("Boolean:", True)

# Print with concatenation
greeting = "Hello"
name = "Python"
print(greeting + " " + name)

# Print with line breaks (escape character)
print("Line 1\nLine 2\nLine 3")
