# Run: python3 functions/parameters.py
# Parameters Examples
# Parameters allow functions to accept data

# Function with one parameter
def greet(name):
    print(f"Hello, {name}!")

greet("Alice")
greet("Bob")
print()

# Function with multiple parameters
def add(a, b):
    result = a + b
    print(f"{a} + {b} = {result}")

add(5, 3)
add(10, 20)
print()


